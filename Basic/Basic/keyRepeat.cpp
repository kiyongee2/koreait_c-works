#include <iostream>
using namespace std;

int main()
{
	string key;  //입력할 문자 저장

	while (true) {
		cout << "계속 반복할까요(y/n 입력)? ";
		cin >> key;

		if (key.compare("y") == 0 || key.compare("Y") == 0) {
			cout << "계속 반복!\n";
		}
		else if (key.compare("n") == 0 || key.compare("N") == 0) {
			cout << "반복 중단\n";
			break; //while문 반복 종료
		}
		else {
			cout << "잘못된 입력입니다! 다시 입력하세요!\n";
		}
	}

	return 0;
}